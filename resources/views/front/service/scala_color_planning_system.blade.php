                <h2>


                  The Scala name code

                </h2>


              </div>





              <p>Color shade, saturation and lightness are the three parameters on which the code of the Scala color
                shades is based. Each individual color shade is clearly identified by the Scala name code as a six-digit
                sequence. This code is designed as a open system to enable updates and additions. The first two digits
                describe the color shade family. The 32 color families are coded in groups of three with the digits 03
                to 96. The two subsequent digits provide information on the color shade saturation. The last two digits
                define its lightness.</p>














