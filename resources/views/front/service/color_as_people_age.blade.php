                    <h3>DIN 18040 – Construction of accessible buildings – Design principles Part 1: Publicly accessible
                        buildings</h3>The definition of accessibility outlined in §4 of the Law on the Equal Treatment
                    of
                    Disabled Persons (BGG) also includes access to visual information. This information is accessible if
                    <ul>
                        <li>it can be obtained in the usual manner,</li>
                        <li>with no particular obstacles</li>
                        <li>and without help from others</li>
                    </ul>
                    <p>. An accessible design must therefore be achieved with the following:</p>
                    <ul>
                        <li>Information is published in a way that makes it available to the largest possible group of
                            people,
                            with and without disabilities</li>
                        <li>Special solutions should be avoided where possible</li>
                        <li>Improvements for people with visual impairments generally also increase the comfort and
                            safety of
                            the non-visually impaired.</li>
                    </ul>
                    <p>DIN 18040 replaces DIN 18024-2:1996-11. Each federal state is individually responsible for
                        incorporating the standard or individual points in the technical construction regulations!</p>
                    <h3>DIN 32975 – Designing visual information in the public area for accessible use</h3>In the past,
                    people
                    with visual impairments hardly played any role at all in the world of standards. DIN 32975 defines
                    what
                    constitutes accessibility from the point of view of people with visual impairments for the first
                    time. It
                    must be applied wherever an accessible design is required. A building that can be accessed with a
                    wheelchair, but does not fulfill the requirements of DIN 32975, can not be classed as accessible.
                    The main
                    subject of this standard is the luminance contrast, that must comply with certain minimum values.
                    Although
                    colors are mentioned, they can only take on a supporting role and can never balance out a low
                    luminance
                    contrast. It assumes that perception of information is always the result of a variety of factors.
                    For
                    defined luminance contrasts, these are the lighting (adaptation luminance), the font or character
                    size and
                    the observation distance.














